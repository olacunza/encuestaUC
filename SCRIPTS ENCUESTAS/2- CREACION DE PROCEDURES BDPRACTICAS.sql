		 ,T1.FECHA_INICIO
		 ,T1.FECHA_FIN
	FROM ENCUESTA.tblEncuestaAsignatura T1
	LEFT JOIN ENCUESTA.tblTipoEncuesta T2 ON T1.TIPO_ENCUESTA = T2.TIPO_ENCUESTA_ID		--CAMBIAR POR INNER EN PROD
	WHERE T1.ENCUESTA_ID = @ENCUESTA_ID

	SELECT
		T2.BLOQUE_ID
		,T2.TITULO_BLOQUE
		,T2.ORDEN
	FROM ENCUESTA.tblEncuestaAsignatura T1
	INNER JOIN ENCUESTA.tblEncuestaBloqueAsignatura T2 ON T1.ENCUESTA_ID = T2.ENCUESTA_ID
	WHERE T1.ENCUESTA_ID = @ENCUESTA_ID

