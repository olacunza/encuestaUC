NRO		FECHA		USUARIO		MODIFICACION
001		DD/MM/YYYY	
===========================================================*/
CREATE PROCEDURE ENCUESTA.sp_ListarPlantillaEncuestas
(
    @PageNumber INT = 1,
    @PageSize   INT = 10
)
AS
BEGIN
SET NOCOUNT ON;

	;WITH CTE AS
		(
			SELECT
				 T1.ENCUESTA_ID
				,T1.NOMBRE_ENCUESTA
				,T1.DESCRIPCION_ENCUESTA
				,T2.NOMBRE_ENCUESTA AS NOMBRE_TIPO_ENCUESTA
				,T1.FECHA_CREACION
			FROM ENCUESTA.tblEncuestaPlantilla T1
			INNER JOIN ENCUESTA.tblTipoEncuesta T2 ON T1.TIPO_ENCUESTA = T2.TIPO_ENCUESTA_ID
			WHERE T1.ESTADO = 1
		)
		SELECT *
		FROM CTE
		ORDER BY FECHA_CREACION DESC
		OFFSET (@PageNumber - 1) * @PageSize ROWS
		FETCH NEXT @PageSize ROWS ONLY;

END

GO

/*===========================================================
NOMBRE: ENCUESTA.sp_ListarAsignaturaEncuestas
FECHA: 18/10/2025
AUTOR: ORESTES LA CUNZA - YACURUNA TECHNOLOGIES
OBJETIVO: 
MODIFICACIONES:
NRO		FECHA		USUARIO		MODIFICACION
001		DD/MM/YYYY	
===========================================================*/
CREATE PROCEDURE ENCUESTA.sp_ListarAsignaturaEncuestas
(
	@SECCION    NVARCHAR(50) = NULL,
    @MODULO     NVARCHAR(50) = NULL,
    @DOCENTE    NVARCHAR(100) = NULL,
    @FECHA_INICIO DATETIME = NULL,
    @FECHA_FIN   DATETIME = NULL,
    @PAGENUMBER INT = 1,
    @PAGESIZE   INT = 10
)
AS
BEGIN
SET NOCOUNT ON;

	;WITH CTE AS
		(
			SELECT
				ENCUESTA_ID
				,SEDE
				,PERIODO
				,TIPO_PROGRAMA
				,SECCION
				,MODULO
				,FECHA_ENVIO
			FROM ENCUESTA.tblEncuestaAsignatura
			WHERE 1 = 1
				AND (NULLIF(@SECCION, '') IS NULL OR SECCION = @SECCION)
				AND (NULLIF(@MODULO, '') IS NULL OR MODULO = @MODULO)
				AND (NULLIF(@DOCENTE, '') IS NULL OR DOCENTE = @DOCENTE)
				AND (@FECHA_INICIO IS NULL OR @FECHA_INICIO = '1900-01-01' OR FECHA_ENVIO >= @FECHA_INICIO)
				AND (@FECHA_FIN IS NULL OR @FECHA_FIN = '1900-01-01' OR FECHA_ENVIO <= @FECHA_FIN)
		)
		SELECT *
		FROM CTE
		ORDER BY FECHA_ENVIO DESC
		OFFSET (@PageNumber - 1) * @PageSize ROWS
		FETCH NEXT @PageSize ROWS ONLY;

END

GO

