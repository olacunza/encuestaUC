		AND T5.ENCUESTA_PREGUNTA_ID = T3.PREGUNTA_ID
	WHERE	1 = 1
			AND ISJSON(T5.VALOR_RESPUESTA) = 1
			AND JSON_VALUE(T5.VALOR_RESPUESTA, '$.score') IS NOT NULL
	GROUP BY T2.BLOQUE_ID
		,T2.TITULO_BLOQUE
		,T3.TEXTO_PREGUNTA
		,T5.ENCUESTA_PREGUNTA_ID

