	FROM ENCUESTA.tblEncuestaAsignaturaAlumno
	WHERE 1 = 1
		AND ENCUESTA_ASIGNATURA_ID = @ENCUESTA_ID	--25
		AND DNI_ENCUESTADO = @DNI_ENCUESTADO	--'06602128'

	IF @EXISTE = 0
        SET @ENCUESTA_ID = -1;

	SELECT
		 T1.ENCUESTA_ID
